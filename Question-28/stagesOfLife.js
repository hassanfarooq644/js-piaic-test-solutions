//stages of life
let user_age = 29;
if (user_age < 2) {
    console.log("Person is Baby")
} else if (user_age >= 2 && user_age < 4) {
    console.log("Person is a Todler")
} else if (user_age >= 4 && user_age < 13) {
    console.log("Person is a Kid")
} else if (user_age >= 13 && user_age < 20) {
    console.log("Person is a Teenager")
} else if (user_age >= 20 && user_age < 65) {
    console.log("Person is an adult")
} if (user_age >= 65) {
    console.log("Person is an elder")
}
